/** i18n key → seçili dilde metin (yoksa EN'e, o da yoksa TR'ye fallback). */
export function t(key: string, lang: LanguageCode = 'tr'): string {
  const dict = STRINGS[lang] ?? tr;
  return dict[key] ?? en[key] ?? tr[key] ?? key;
}

