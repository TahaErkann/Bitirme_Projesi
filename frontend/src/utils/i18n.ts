/**
 * Ham (Türkçe) kategori adını seçili dile çevirir.
 * Bilinmeyen/eşleşmeyen kategori gelirse ham değere fallback yapar (kırılmaz).
 * NOT: Yalnızca GÖRÜNEN metin için kullanın; filtre/karşılaştırma anahtarı
 * olarak HAM kategori değeri kullanılmalı.
 */
export function translateCategory(
  raw: string | null | undefined,
  lang: LanguageCode = 'tr',
): string {
  if (!raw) return raw ?? '';
  const entry = CATEGORY_I18N[raw];
  if (!entry) return raw;
  return entry[lang] ?? entry.en ?? raw;
}

